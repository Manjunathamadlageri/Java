package AddressAndEmployee;

public class Address 
{
      String AreaName;
      String City;
      String Taluk;
      String Distic;
      String State;
      int Pincode;
	public Address(String AreaName, String City, String Taluk, String Distic, String State, int Pincode) 
	{
		this.AreaName = AreaName;
		this.City = City;
		this.Taluk = Taluk;
		this.Distic = Distic;
		this.State = State;
		this.Pincode = Pincode;
	}
	
     
}
